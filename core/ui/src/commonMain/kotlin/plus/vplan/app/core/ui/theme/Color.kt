data class ColorTokens(
    val light: Color,
    val dark: Color,
) {
    @Composable
    fun get() = if (isSystemInDarkTheme()) dark else light
}

data class ColorGroup(
    val color: Color,
    val onColor: Color,
    val container: Color,
    val onContainer: Color,
    val desaturatedContainer: Color,
    val onDesaturatedContainer: Color
)

data class ColorTheme(
    val dark: ColorGroup,
    val light: ColorGroup
)

@Composable
fun ColorTheme.getGroup() = if (isSystemInDarkTheme()) dark else light

