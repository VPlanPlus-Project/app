        dark = yellowContainerDark,
    ),
    ColorToken.OnYellowContainer to ColorTokens(
        light = onYellowContainerLight,
        dark = onYellowContainerDark,
    )
)

data class ColorTokens(
    val light: Color,
    val dark: Color,
) {
    @Composable
    fun get() = when (isSystemInDarkTheme()) {
        true -> dark
        false -> light
    }
}

data class ColorGroup(
    val color: Color,
    val onColor: Color,
    val container: Color,
    val onContainer: Color
)

data class ColorTheme(
    val dark: ColorGroup,
    val light: ColorGroup
) {
    @Composable
    fun getGroup() = if (isSystemInDarkTheme()) dark else light
}

