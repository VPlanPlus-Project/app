enum class ColorToken {
    Green, OnGreen, GreenContainer, OnGreenContainer,
    Yellow, OnYellow, YellowContainer, OnYellowContainer
}

enum class CustomColor {
    Green, Yellow, Cyan, DarkPurple, Red, GreenGray, Violet, WineRed, Blue
}

